  /// Converts any traditional characters in [text] to their simplified forms.
  /// Characters with no mapping (already simplified, or identical in both
  /// scripts) are returned unchanged.
  ///
  /// Called by [ScannerService] to normalise OCR output before matching
  /// against the card database (which stores names in simplified Chinese).
  static String toSimplified(String text) {
    if (text.isEmpty) return text;
    final buf = StringBuffer();
    for (final rune in text.runes) {
      final ch = String.fromCharCode(rune);
      buf.write(_tradToSimp[ch] ?? ch);
    }
    return buf.toString();
  }

  /// Converts any simplified characters in [text] to their traditional forms.
  static String toTraditional(String text) {
    if (text.isEmpty) return text;
    final buf = StringBuffer();
    for (final rune in text.runes) {
      final ch = String.fromCharCode(rune);
      buf.write(_simpToTrad[ch] ?? ch);
    }
    return buf.toString();
  }

  /// Normalises [text] to simplified Chinese.
  /// Alias for [toSimplified] — the primary method used by [ScannerService].
  static String normalise(String text) => toSimplified(text);

  /// Returns true if [text] contains any traditional Chinese characters
  /// that have simplified counterparts in this map.
  static bool containsTraditional(String text) {
    return text.runes.any(
      (r) => _tradToSimp.containsKey(String.fromCharCode(r)),
    );
  }
